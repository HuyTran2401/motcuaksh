          Địa chỉ:  ấp An Khương, Thị trấn Kế Sách, huyện Kế Sách, tỉnh Sóc Trăng.<br>
          <b>1. Tên gọi di tích:</b> Đình thần <b>Nguyễn Trung Trực.</b><br>
          <b>2. Địa điểm</b> và đường đi đến di tích<br>
          <b>a. Địa điểm:</b> Đình thần Nguyễn Trung trực ấp An Khương Thị trấn Kế sách huyện Kế sách tỉnh Sóc Trăng.<br>
          <b>b. Đường đi đến di tích:</b><br>
          - Từ thành phố Sóc Trăng đến ngã 3 An trạch rẽ phải đi theo tuyến tỉnh lộ 932 đến bến xe thị trấn Kế Sách, rẽ phải theo đường Phan Văn Hùng chạy qua cầu Kế sách khoảng 150m là đến đình.<br>
          - Từ quốc lộ Nam Sông hậu có thể đi theo 03 đường vào thị trấn Kế Sách: vào từ ngã ba Cái Trâm, vào từ ngã ba Cái Trưng ( An Lạc Tây )  hoặc vào từ ngã tư Trà Ếch 
         ( Nhơn Mỹ ) để vào thị trấn Kế Sách bằng đường tỉnh 932 để đến địa chỉ đình như nói trên.
          <br>
         <b>3. Tự phân loại di tích:</b> với lịch sử hình thành, kiến trúc và quy mô cơ sở vật chất hiện tại, có thể phân loại đình này thuộc loại hình di tích lịch sử -  văn hóa.<br>
         <b>4. Sự kiện, nhân vật lịch sử, đặc điểm của di tích:</b><br>
         &nbsp;<b>4.1. Khái quát về thị trấn Kế Sách:</b><br>
         &nbsp;&nbsp;Thị trấn Kế Sách trước đây là nơi chú đóng của chế độ cũ, với tên gọi là quận Kế Sách. Sau khi tiếp quản vào ngày 30/4/1975 đã trở thành trung tâm kinh tế, chính trị, văn hóa xã hội huyện Kế Sách cho đến ngày nay.<br>
         Về vị trí địa lý dân cư, hơn vị hành chính:<br>
         - Phía Đông giáp xã An Mỹ;<br>
         - Phía tây giáp xã Kế Thành;<br>
         - Phía Nam giáp xã Phú Tâm huyện Châu Thành;<br>
         - Phía bắc giáp xã Thới An Hội.<br>
         &nbsp;&nbsp;Hiện nay thị trấn Kế sách 6 ấp với diện tích tự nhiên là 1462,86 ha; dân số 16284 người, trong đó dân tộc Kinh 9.211 người dân tộc Khmer 6244 người dân tộc Hoa 829 người.<br>
         &nbsp;<b>4.2. lịch sử hình thành ngôi đình:</b><br>
         &nbsp;&nbsp;Cụ Nguyễn Trung trực tên thật là Nguyễn Văn Lịch, sinh năm 1939 tại tỉnh Bình Định ( Trung bộ Việt Nam). Khi thực dân Pháp xâm lược nước ta, gia đình của di cư vào Nam và định cư ở thôn Bình Nhựt, nay là xã Bình Đức, huyện Bến Lức, tỉnh Long An. <br>
         &nbsp;&nbsp;Là người mưu trí, võ nghệ tinh thông, vào lòng quả cảm, cụ đã cùng nghĩa quân lập nên những chiến công quan dội trong cuộc chiến chống Pháp, như lịch sử dân tộc đã ghi nhận.<br>
           &nbsp;&nbsp;Để tỏ lòng thành kính đối với vị thủ lĩnh kiên cường của phong trào chiến Thực dân Pháp, người dân nhiều nơi đã dựng đền thờ để tôn vinh, tưởng nhớ Nguyễn Trung Trực. Trên địa bàn huyện Kế Sách, trong thời kỳ kháng chiến chống Pháp và chống Mỹ cứu nước, cũng có nhiều nơi đã lập đền thờ cụ bền bĩ nuôi dưỡng ý chí đấu tranh cách mạng, tinh thần bất khuất trống giặc ngoại xâm cho các thế hệ. Trong đó tiêu biểu phải kể đến Đình thần Nguyễn Trung Trực tại ấp An Khương, Thị trấn Kế Sách, huyện Kế sách tỉnh Sóc Trăng.  <br>
           &nbsp;&nbsp;Đình thần Nguyễn Trung Trực tọa lạc tại ấp An Khương, Thị trấn Kế Sách, có diện tích chung là: diện tích 1.083,4 m theo thửa đất số 24, bản đồ 06.  <br>
           Đình được hình thành vào khoảng năm 1960 do ông hiệu Sương xây dựng bằng bê tông bao gồm: cổng đình, sân đình, bên phải sân đình làm miếu thành hoàng, bên trái là miếu Năm mẹ, ở giữa thờ thần Nông, ông Hổ. Bên trong đình, Chánh điện thờ cụ Nguyễn Trung Trực, tả ban, hữu ban, phía sau thờ Cửu Huyền thất tổ, bên thiên thờ ảnh bác.<br>
           &nbsp;&nbsp;Sau khi xây dựng đình thì ông hiệu Sương thuê ông Cẩm Hùng rồi đến Hồ Văn hơn ( tức ông Hai Đình) trông coi. Sau khi ông Hai Định mất thì ông Hồ Bá Triều con ông Hai Đình trông coi. Đến khoảng năm 1990 thì ông Trịnh Đồng Lường tiếp nhận định và thành lập bang hội đình, duy trì các nghi lễ hàng năm. Sau đó, ông Hồ Bá Triều tiếp tục trông coi đình. Đến năm 2000 thì ông Phạm Minh Châu (tức ông Năm Chân) tiếp nhận, sửa chữa đình và thành lập Ban hội đình mới. Năm 2018 do tình hình sức khỏe của ông Năm Chân đã yếu, cũng như tình hình tài chính của Ban hội đình có khó khăn trong việc tu sửa và duy trì các hoạt động của đình, nên ông Năm Chân cùng thành viên Ban hội đình thống nhất Giao đình lại cho ông Đinh Văn vui.<br>
            &nbsp;&nbsp;Ông đinh Văn vui tiếp nhận đình trong tình trạng cơ sở vật chất đã xuống cấp nghiêm trọng, không gian kiến trúc hạn hẹp, nên ông đã tiến hành phối hợp với chính quyền địa phương mở rộng không gian, đầu tư xây dựng mới lại đình, công trình hoàn thành vào năm 2020, được khang trang như hiện nay.<br>
            <b>5. Về yếu tố liên quan đến lịch sử chống giặc ngoại xâm của đình:</b><br>
            &nbsp;&nbsp;Vào thời điểm cuối tháng 9, đầu tháng 10 năm 1974, lúc này, cuộc chiến tranh với ngụy quyền Sài Gòn của quân và dân ta đang ở giai đoạn quyết liệt. Huyện quỹ Kế sách đã chỉ đạo cho lực lượng An ninh vũ trang huyện phải tiêu diệt cho bằng được những tên Bình Định và các trưởng ấp ác ôn, giải tán phòng vệ dân sự, tạo điều kiện phối hợp tổ chức các mũi giáp công, góp phần tiến tới tiêu diệt và bức hàng địch trên diện rộng, nhằm giải phóng quê hương bằng chiến dịch Hồ Chí Minh lịch sử năm 1975 theo chỉ đạo của cấp trên.<br>
            Nhận nhiệm vụ giao, được lực lượng An ninh vũ trang huyện cùng hai cơ sở cách mạng lên kế hoạch đánh tiêu diệt Đồn Liên toán phòng vệ thị trấn Kế Sách (gần Trường Tiểu Học Kế Sách 1 hiện nay). Trong đó cơ sở (1) là Chú 9 Lùn, phụ trách vẽ sơ đồ đồn Liên toán phòng vệ; cơ sở (2) là anh Buôl, lính của đại hội 575, phụ trách nắm tình hình địch.
            &nbsp;&nbsp;Lực lượng được giao nhiệm vụ tham gia trận đánh Đ/c Nguyễn Hoàng Ngữ (đã nghỉ hưu hiện đang là bí thư Kim trưởng ban nhân dân ấp An Nhơn, xã Thới An Hội), cùng với 02 lực lượng biệt Động thị trấn và 03 lực lượng An ninh vũ trang huyện, do Đ/c Võ Văn Nhị trực tiếp chỉ huy.<br>
            Để tổ chức trận đánh đảm bảo thắng lợi, trước trận đánh, lực lượng tham gia phải truy sát rất kỹ. Chỉ huy và lực lượng vũ trang huyện đã chọn cơ sở Đình thần Nguyễn Trung Trực làm nơi đồn trú, ém quân trong 03 đợt, mỗi đợt 01 ngày đêm để đi đều nghiên, trinh sát, từ đó lên ký sách tác Chiến.<br>
            Sau khi kế hoạch hoàn thành, vào đêm 2/10/1974 toàn lực lượng xuất phát từ đình thần Nguyễn Trung Trực, chia làm các buổi tiến công dồn trong cả đêm. Trận đánh đã kết thúc thắng lợi, tiêu diệt được 05 tên địch, trong đó có 01 tên trưởng ấp ác ôn, thu 04 khẩu súng các loại, lập nên chiếm công quan trọng, làm tiền đề rất tốt cho các trận chiến đấu tiếp theo.<br>
            Theo các nhân chứng còn sống (trong đó Đ/c Nguyễn Hoàng Ngữ), quá trình lực lượng vũ trang của ta ém quân, đồn trú tại đình để điều nghiên, trinh sát, tập trung lực lượng, đã được quản lý đình và một số bà con cảm tình cách mạng xung quanh nhiệt tình che chở, nuôi dưỡng và đảm bảo tuyệt đối bí mật. Nhờ vậy, trận đánh đã thắng lợi tốt đẹp, hoàn thành xuất sắc nhiệm vụ yêu cầu chỉ đạo của Huyện ủy lúc bấy giờ.<br>
            <b>6. Sinh hoạt văn hóa tín ngưỡng của đình.</b><br>
            &nbsp;&nbsp;Từ khi hình thành đến nay hàng trăm đình thần Nguyễn Trung Trực Thị Trấn Kế Sách luôn duy trì được các nghi thức:<br>
            &nbsp;&nbsp;-Tháng 01 lễ cầu an;<br>
            &nbsp;&nbsp;-Tháng 02 lễ cúng bà Năm mẹ;<br>
            &nbsp;&nbsp;-Tháng 03 lễ cúng Thành Hoàng;<br>
            &nbsp;&nbsp;-Tháng 04 lễ cúng hạ điền;<br>
            &nbsp;&nbsp;-Tháng 08 giỗ ông Nguyễn Trung Trực;<br>
            &nbsp;&nbsp;-Tháng 11 lễ cúng thượng điền;<br>
            &nbsp;&nbsp;-Tháng 12 Tất niên.<br>
